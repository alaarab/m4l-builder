"""Velocity curve display engine for jsui.

Renders a draggable velocity response curve inside Max's jsui object using
mgraphics. X axis is input velocity (0-127), Y axis is output velocity (0-127).
Four control points define the curve shape.

Inlets:
    0 -- Control point updates (x1, y1, x2, y2 floats 0-1)

Outlets:
    0 -- x1 (float 0-1) on curve change
    1 -- y1 (float 0-1) on curve change
    2 -- x2 (float 0-1) on curve change
    3 -- y2 (float 0-1) on curve change
    4 -- bang on any curve change
"""

VELOCITY_CURVE_INLETS = 1
VELOCITY_CURVE_OUTLETS = 5


def velocity_curve_display_js(*, bg_color="0.07, 0.07, 0.08, 1.0",
                               curve_color="0.45, 0.75, 0.65, 1.0",
                               point_color="1.0, 0.85, 0.4, 1.0"):
    """Return JavaScript source for a velocity curve display (Max jsui).

    Args:
        bg_color: Background color (RGBA string).
        curve_color: Curve line color (RGBA string).
        point_color: Draggable control point color (RGBA string).

    Returns:
        JavaScript source code string (ES5 compatible).
    """
    return (
        "// Velocity Curve Display — generated by m4l-builder\n"
        "// ES5 JavaScript for Max jsui mgraphics\n"
        "\n"
        "mgraphics.init();\n"
        "mgraphics.relative_coords = 0;\n"
        "mgraphics.autofill = 0;\n"
        "\n"
        "inlets = 1;\n"
        "outlets = 5;\n"
        "\n"
        "// --- State ---\n"
        "// Control points as normalized 0-1 coordinates\n"
        "var pt_x1 = 0.25;\n"
        "var pt_y1 = 0.35;\n"
        "var pt_x2 = 0.75;\n"
        "var pt_y2 = 0.65;\n"
        "var active_pt = -1;\n"
        "\n"
        "// --- Constants ---\n"
        "var POINT_RADIUS = 6;\n"
        "var PADDING = 10;\n"
        "var AXIS_LABEL_SIZE = 9;\n"
        "\n"
        "// --- Input handler ---\n"
        "function msg_list() {\n"
        "    if (arguments.length >= 4) {\n"
        "        pt_x1 = Math.max(0, Math.min(1, arguments[0]));\n"
        "        pt_y1 = Math.max(0, Math.min(1, arguments[1]));\n"
        "        pt_x2 = Math.max(0, Math.min(1, arguments[2]));\n"
        "        pt_y2 = Math.max(0, Math.min(1, arguments[3]));\n"
        "        mgraphics.redraw();\n"
        "    }\n"
        "}\n"
        "\n"
        "// --- Helpers ---\n"
        "function get_area() {\n"
        "    var w = mgraphics.size[0];\n"
        "    var h = mgraphics.size[1];\n"
        "    return {\n"
        "        x: PADDING,\n"
        "        y: PADDING,\n"
        "        w: w - 2 * PADDING,\n"
        "        h: h - 2 * PADDING\n"
        "    };\n"
        "}\n"
        "\n"
        "function to_screen(nx, ny, area) {\n"
        "    return {\n"
        "        x: area.x + nx * area.w,\n"
        "        y: area.y + (1.0 - ny) * area.h\n"
        "    };\n"
        "}\n"
        "\n"
        "function to_norm(sx, sy, area) {\n"
        "    return {\n"
        "        nx: Math.max(0, Math.min(1, (sx - area.x) / area.w)),\n"
        "        ny: Math.max(0, Math.min(1, 1.0 - (sy - area.y) / area.h))\n"
        "    };\n"
        "}\n"
        "\n"
        "function hit_test(sx, sy, px, py, area) {\n"
        "    var p = to_screen(px, py, area);\n"
        "    var dx = sx - p.x;\n"
        "    var dy = sy - p.y;\n"
        "    return (dx * dx + dy * dy) <= (POINT_RADIUS + 4) * (POINT_RADIUS + 4);\n"
        "}\n"
        "\n"
        "// --- Mouse handling ---\n"
        "function onclick(x, y, but, cmd, shift, capslock, option, ctrl) {\n"
        "    var area = get_area();\n"
        "    active_pt = -1;\n"
        "    if (hit_test(x, y, pt_x1, pt_y1, area)) {\n"
        "        active_pt = 0;\n"
        "    } else if (hit_test(x, y, pt_x2, pt_y2, area)) {\n"
        "        active_pt = 1;\n"
        "    }\n"
        "    if (active_pt >= 0) {\n"
        "        move_active_pt(x, y, area);\n"
        "    }\n"
        "}\n"
        "\n"
        "function ondrag(x, y, but, cmd, shift, capslock, option, ctrl) {\n"
        "    if (but === 0) {\n"
        "        active_pt = -1;\n"
        "        return;\n"
        "    }\n"
        "    if (active_pt < 0) {\n"
        "        return;\n"
        "    }\n"
        "    var area = get_area();\n"
        "    move_active_pt(x, y, area);\n"
        "}\n"
        "\n"
        "function move_active_pt(x, y, area) {\n"
        "    var n = to_norm(x, y, area);\n"
        "    if (active_pt === 0) {\n"
        "        pt_x1 = n.nx;\n"
        "        pt_y1 = n.ny;\n"
        "    } else {\n"
        "        pt_x2 = n.nx;\n"
        "        pt_y2 = n.ny;\n"
        "    }\n"
        "    outlet(0, pt_x1);\n"
        "    outlet(1, pt_y1);\n"
        "    outlet(2, pt_x2);\n"
        "    outlet(3, pt_y2);\n"
        "    outlet(4, 'bang');\n"
        "    mgraphics.redraw();\n"
        "}\n"
        "\n"
        "// --- Drawing ---\n"
        "function paint() {\n"
        "    var w = mgraphics.size[0];\n"
        "    var h = mgraphics.size[1];\n"
        "    var area = get_area();\n"
        "\n"
        "    // Background\n"
        f"    mgraphics.set_source_rgba({bg_color});\n"
        "    mgraphics.rectangle(0, 0, w, h);\n"
        "    mgraphics.fill();\n"
        "\n"
        "    // Diagonal reference line (linear response)\n"
        "    mgraphics.set_source_rgba(0.25, 0.25, 0.28, 0.6);\n"
        "    mgraphics.set_line_width(0.5);\n"
        "    mgraphics.move_to(area.x, area.y + area.h);\n"
        "    mgraphics.line_to(area.x + area.w, area.y);\n"
        "    mgraphics.stroke();\n"
        "\n"
        "    // Grid lines\n"
        "    mgraphics.set_source_rgba(0.2, 0.2, 0.22, 0.35);\n"
        "    mgraphics.set_line_width(0.5);\n"
        "    var i;\n"
        "    for (i = 1; i < 4; i++) {\n"
        "        var gx = area.x + (i / 4.0) * area.w;\n"
        "        mgraphics.move_to(gx, area.y);\n"
        "        mgraphics.line_to(gx, area.y + area.h);\n"
        "        mgraphics.stroke();\n"
        "        var gy = area.y + (i / 4.0) * area.h;\n"
        "        mgraphics.move_to(area.x, gy);\n"
        "        mgraphics.line_to(area.x + area.w, gy);\n"
        "        mgraphics.stroke();\n"
        "    }\n"
        "\n"
        "    // Bezier curve through the 4 points: (0,0) -> cp1 -> cp2 -> (1,1)\n"
        "    var p0 = to_screen(0.0, 0.0, area);\n"
        "    var p1 = to_screen(pt_x1, pt_y1, area);\n"
        "    var p2 = to_screen(pt_x2, pt_y2, area);\n"
        "    var p3 = to_screen(1.0, 1.0, area);\n"
        "\n"
        f"    mgraphics.set_source_rgba({curve_color});\n"
        "    mgraphics.set_line_width(1.5);\n"
        "    mgraphics.move_to(p0.x, p0.y);\n"
        "    mgraphics.curve_to(p1.x, p1.y, p2.x, p2.y, p3.x, p3.y);\n"
        "    mgraphics.stroke();\n"
        "\n"
        "    // Control point handles (dashed lines from endpoints)\n"
        "    mgraphics.set_source_rgba(0.35, 0.35, 0.38, 0.5);\n"
        "    mgraphics.set_line_width(0.5);\n"
        "    mgraphics.move_to(p0.x, p0.y);\n"
        "    mgraphics.line_to(p1.x, p1.y);\n"
        "    mgraphics.stroke();\n"
        "    mgraphics.move_to(p3.x, p3.y);\n"
        "    mgraphics.line_to(p2.x, p2.y);\n"
        "    mgraphics.stroke();\n"
        "\n"
        "    // Control point dots\n"
        f"    mgraphics.set_source_rgba({point_color});\n"
        "    mgraphics.arc(p1.x, p1.y, POINT_RADIUS, 0, Math.PI * 2);\n"
        "    mgraphics.fill();\n"
        "    mgraphics.arc(p2.x, p2.y, POINT_RADIUS, 0, Math.PI * 2);\n"
        "    mgraphics.fill();\n"
        "\n"
        "    // Endpoint dots (smaller)\n"
        f"    mgraphics.set_source_rgba({curve_color});\n"
        "    mgraphics.arc(p0.x, p0.y, 3, 0, Math.PI * 2);\n"
        "    mgraphics.fill();\n"
        "    mgraphics.arc(p3.x, p3.y, 3, 0, Math.PI * 2);\n"
        "    mgraphics.fill();\n"
        "}\n"
    )
