"""Peaking EQ band display engine for jsui.

Generates JavaScript code that renders a draggable parametric EQ display
inside Max's jsui object using mgraphics. Up to 8 bands are shown as
draggable control points on a frequency response curve.

Inlets:
    0 -- Band data: list [band_index, freq, gain, q]

Outlets:
    0 -- Band index (int)
    1 -- Frequency (float)
    2 -- Gain (float)
    3 -- Q (float)
"""

from ._graph_colors import (
    DEFAULT_GRAPH_PLOT_BORDER_COLOR,
    DEFAULT_GRAPH_PLOT_COLOR,
    resolve_graph_panel_color,
)

PEAKING_EQ_DISPLAY_INLETS = 1
PEAKING_EQ_DISPLAY_OUTLETS = 4


def peaking_eq_display_js(*,
                          bg_color=DEFAULT_GRAPH_PLOT_COLOR,
                          panel_color=None,
                          plot_border_color=DEFAULT_GRAPH_PLOT_BORDER_COLOR,
                          line_color="0.45, 0.75, 0.65, 1.0",
                          point_color="0.9, 0.6, 0.3, 1.0"):
    """Return JavaScript source for a peaking EQ band display (Max jsui).

    Inlet 0 receives [band_index, freq, gain, q] to set a band.
    Clicking and dragging moves a band: x maps to frequency (log scale),
    y maps to gain. Scroll/drag changes Q. Outputs the updated band on
    outlets 0-3.

    Args:
        bg_color: Plot background color (RGBA string).
        panel_color: Outer panel color. Defaults to Ableton-style grey unless
            bg_color is transparent, in which case the shell stays transparent.
        plot_border_color: Outline color for the plot region.
        line_color: Frequency response curve color (RGBA string).
        point_color: Draggable band point color (RGBA string).

    Returns:
        JavaScript source code string (ES5 compatible).
    """
    panel_color = resolve_graph_panel_color(bg_color, panel_color)
    lc = [s.strip() for s in line_color.split(",")]
    line_fill = lc[0] + ", " + lc[1] + ", " + lc[2] + ", 0.15"

    return (
        "// Peaking EQ Display - generated by m4l-builder\n"
        "// ES5 JavaScript for Max jsui mgraphics\n"
        "\n"
        "mgraphics.init();\n"
        "mgraphics.relative_coords = 0;\n"
        "mgraphics.autofill = 0;\n"
        "\n"
        "inlets = 1;\n"
        "outlets = 4;\n"
        "\n"
        "// --- Constants ---\n"
        "var MAX_BANDS = 8;\n"
        "var MIN_FREQ = 20;\n"
        "var MAX_FREQ = 20000;\n"
        "var MIN_GAIN = -18;\n"
        "var MAX_GAIN = 18;\n"
        "var MIN_Q = 0.1;\n"
        "var MAX_Q = 20;\n"
        "var LOG_MIN = Math.log(MIN_FREQ) / Math.LN10;\n"
        "var LOG_MAX = Math.log(MAX_FREQ) / Math.LN10;\n"
        "var LOG_RANGE = LOG_MAX - LOG_MIN;\n"
        "var PADDING = 6;\n"
        "var POINT_RADIUS = 5;\n"
        "var CURVE_STEPS = 200;\n"
        "\n"
        "// --- State ---\n"
        "var bands = [];\n"
        "var selected = -1;\n"
        "var i;\n"
        "for (i = 0; i < MAX_BANDS; i++) {\n"
        "    bands.push({ freq: 1000, gain: 0, q: 1.0, active: 0 });\n"
        "}\n"
        "\n"
        "// --- Input handlers ---\n"
        "function msg_list() {\n"
        "    var args = arrayfromargs(arguments);\n"
        "    if (args.length >= 4) {\n"
        "        var idx = Math.round(args[0]);\n"
        "        if (idx >= 0 && idx < MAX_BANDS) {\n"
        "            bands[idx].freq = args[1];\n"
        "            bands[idx].gain = args[2];\n"
        "            bands[idx].q = args[3];\n"
        "            bands[idx].active = 1;\n"
        "            mgraphics.redraw();\n"
        "        }\n"
        "    }\n"
        "}\n"
        "\n"
        "// --- Coordinate helpers ---\n"
        "function freq_to_x(freq, plot_x, plot_w) {\n"
        "    var logf = Math.log(Math.max(freq, MIN_FREQ)) / Math.LN10;\n"
        "    return plot_x + ((logf - LOG_MIN) / LOG_RANGE) * plot_w;\n"
        "}\n"
        "\n"
        "function x_to_freq(x, plot_x, plot_w) {\n"
        "    var t = (x - plot_x) / plot_w;\n"
        "    if (t < 0) t = 0;\n"
        "    if (t > 1) t = 1;\n"
        "    return Math.pow(10, LOG_MIN + t * LOG_RANGE);\n"
        "}\n"
        "\n"
        "function gain_to_y(gain, plot_y, plot_h) {\n"
        "    return plot_y + (1.0 - (gain - MIN_GAIN) / (MAX_GAIN - MIN_GAIN)) * plot_h;\n"
        "}\n"
        "\n"
        "function y_to_gain(y, plot_y, plot_h) {\n"
        "    var t = 1.0 - (y - plot_y) / plot_h;\n"
        "    if (t < 0) t = 0;\n"
        "    if (t > 1) t = 1;\n"
        "    return MIN_GAIN + t * (MAX_GAIN - MIN_GAIN);\n"
        "}\n"
        "\n"
        "// Compute EQ gain in dB at frequency f for a single band\n"
        "function band_response(b, f) {\n"
        "    if (!b.active || b.gain === 0) return 0;\n"
        "    var w0 = 2 * Math.PI * b.freq;\n"
        "    var w = 2 * Math.PI * f;\n"
        "    var ratio = w / w0;\n"
        "    if (ratio <= 0) return 0;\n"
        "    var log_ratio = Math.log(ratio) / Math.LN10;\n"
        "    var bw_factor = b.q * 2.302585; // ln(10)\n"
        "    var response = b.gain / (1 + bw_factor * bw_factor * log_ratio * log_ratio);\n"
        "    return response;\n"
        "}\n"
        "\n"
        "function total_response(f) {\n"
        "    var total = 0;\n"
        "    var j;\n"
        "    for (j = 0; j < MAX_BANDS; j++) {\n"
        "        if (bands[j].active) {\n"
        "            total += band_response(bands[j], f);\n"
        "        }\n"
        "    }\n"
        "    return total;\n"
        "}\n"
        "\n"
        "// --- Mouse handling ---\n"
        "function find_band_at(mx, my, plot_x, plot_y, plot_w, plot_h) {\n"
        "    var j;\n"
        "    for (j = 0; j < MAX_BANDS; j++) {\n"
        "        if (!bands[j].active) continue;\n"
        "        var bx = freq_to_x(bands[j].freq, plot_x, plot_w);\n"
        "        var by = gain_to_y(bands[j].gain, plot_y, plot_h);\n"
        "        var dx = mx - bx;\n"
        "        var dy = my - by;\n"
        "        if (dx * dx + dy * dy <= (POINT_RADIUS + 4) * (POINT_RADIUS + 4)) {\n"
        "            return j;\n"
        "        }\n"
        "    }\n"
        "    return -1;\n"
        "}\n"
        "\n"
        "function output_band(idx) {\n"
        "    outlet(0, idx);\n"
        "    outlet(1, bands[idx].freq);\n"
        "    outlet(2, bands[idx].gain);\n"
        "    outlet(3, bands[idx].q);\n"
        "}\n"
        "\n"
        "function onclick(x, y, but, cmd, shift, capslock, option, ctrl) {\n"
        "    var w = mgraphics.size[0];\n"
        "    var h = mgraphics.size[1];\n"
        "    var plot_x = PADDING;\n"
        "    var plot_y = PADDING;\n"
        "    var plot_w = w - 2 * PADDING;\n"
        "    var plot_h = h - 2 * PADDING;\n"
        "    selected = find_band_at(x, y, plot_x, plot_y, plot_w, plot_h);\n"
        "    if (selected >= 0) {\n"
        "        bands[selected].freq = x_to_freq(x, plot_x, plot_w);\n"
        "        bands[selected].gain = y_to_gain(y, plot_y, plot_h);\n"
        "        output_band(selected);\n"
        "        mgraphics.redraw();\n"
        "    }\n"
        "}\n"
        "\n"
        "function ondrag(x, y, but, cmd, shift, capslock, option, ctrl) {\n"
        "    if (but === 0) {\n"
        "        selected = -1;\n"
        "        return;\n"
        "    }\n"
        "    if (selected < 0) return;\n"
        "    var w = mgraphics.size[0];\n"
        "    var h = mgraphics.size[1];\n"
        "    var plot_x = PADDING;\n"
        "    var plot_y = PADDING;\n"
        "    var plot_w = w - 2 * PADDING;\n"
        "    var plot_h = h - 2 * PADDING;\n"
        "    bands[selected].freq = x_to_freq(x, plot_x, plot_w);\n"
        "    bands[selected].gain = y_to_gain(y, plot_y, plot_h);\n"
        "    output_band(selected);\n"
        "    mgraphics.redraw();\n"
        "}\n"
        "\n"
        "function onwheel(x, y, xdelta, ydelta) {\n"
        "    var w = mgraphics.size[0];\n"
        "    var h = mgraphics.size[1];\n"
        "    var plot_x = PADDING;\n"
        "    var plot_y = PADDING;\n"
        "    var plot_w = w - 2 * PADDING;\n"
        "    var plot_h = h - 2 * PADDING;\n"
        "    var idx = find_band_at(x, y, plot_x, plot_y, plot_w, plot_h);\n"
        "    if (idx < 0) idx = selected;\n"
        "    if (idx < 0) return;\n"
        "    bands[idx].q = bands[idx].q + ydelta * 0.1;\n"
        "    if (bands[idx].q < MIN_Q) bands[idx].q = MIN_Q;\n"
        "    if (bands[idx].q > MAX_Q) bands[idx].q = MAX_Q;\n"
        "    output_band(idx);\n"
        "    mgraphics.redraw();\n"
        "}\n"
        "\n"
        "// --- Drawing ---\n"
        "function paint() {\n"
        "    var w = mgraphics.size[0];\n"
        "    var h = mgraphics.size[1];\n"
        "    var plot_x = PADDING;\n"
        "    var plot_y = PADDING;\n"
        "    var plot_w = w - 2 * PADDING;\n"
        "    var plot_h = h - 2 * PADDING;\n"
        "\n"
        "    // Outer shell + plot surface\n"
        f"    mgraphics.set_source_rgba({panel_color});\n"
        "    mgraphics.rectangle(0, 0, w, h);\n"
        "    mgraphics.fill();\n"
        f"    mgraphics.set_source_rgba({bg_color});\n"
        "    mgraphics.rectangle(plot_x, plot_y, plot_w, plot_h);\n"
        "    mgraphics.fill_preserve();\n"
        f"    mgraphics.set_source_rgba({plot_border_color});\n"
        "    mgraphics.set_line_width(1.0);\n"
        "    mgraphics.stroke();\n"
        "\n"
        "    // Zero-gain center line\n"
        "    var zero_y = gain_to_y(0, plot_y, plot_h);\n"
        "    mgraphics.set_source_rgba(0.25, 0.25, 0.27, 0.7);\n"
        "    mgraphics.set_line_width(0.5);\n"
        "    mgraphics.move_to(plot_x, zero_y);\n"
        "    mgraphics.line_to(plot_x + plot_w, zero_y);\n"
        "    mgraphics.stroke();\n"
        "\n"
        "    // Frequency response curve, filled area below zero\n"
        "    var step = plot_w / CURVE_STEPS;\n"
        "    var xs = [];\n"
        "    var ys = [];\n"
        "    var k;\n"
        "    for (k = 0; k <= CURVE_STEPS; k++) {\n"
        "        var px = plot_x + k * step;\n"
        "        var freq = x_to_freq(px, plot_x, plot_w);\n"
        "        var db = total_response(freq);\n"
        "        if (db < MIN_GAIN) db = MIN_GAIN;\n"
        "        if (db > MAX_GAIN) db = MAX_GAIN;\n"
        "        xs.push(px);\n"
        "        ys.push(gain_to_y(db, plot_y, plot_h));\n"
        "    }\n"
        "\n"
        "    // Fill\n"
        f"    mgraphics.set_source_rgba({line_fill});\n"
        "    mgraphics.move_to(xs[0], zero_y);\n"
        "    for (k = 0; k <= CURVE_STEPS; k++) {\n"
        "        mgraphics.line_to(xs[k], ys[k]);\n"
        "    }\n"
        "    mgraphics.line_to(xs[CURVE_STEPS], zero_y);\n"
        "    mgraphics.close_path();\n"
        "    mgraphics.fill();\n"
        "\n"
        "    // Curve stroke\n"
        f"    mgraphics.set_source_rgba({line_color});\n"
        "    mgraphics.set_line_width(1.5);\n"
        "    mgraphics.move_to(xs[0], ys[0]);\n"
        "    for (k = 1; k <= CURVE_STEPS; k++) {\n"
        "        mgraphics.line_to(xs[k], ys[k]);\n"
        "    }\n"
        "    mgraphics.stroke();\n"
        "\n"
        "    // Band control points\n"
        "    var j;\n"
        "    for (j = 0; j < MAX_BANDS; j++) {\n"
        "        if (!bands[j].active) continue;\n"
        "        var bx = freq_to_x(bands[j].freq, plot_x, plot_w);\n"
        "        var by = gain_to_y(bands[j].gain, plot_y, plot_h);\n"
        "        if (j === selected) {\n"
        "            mgraphics.set_source_rgba(1.0, 1.0, 1.0, 0.9);\n"
        "        } else {\n"
        f"            mgraphics.set_source_rgba({point_color});\n"
        "        }\n"
        "        mgraphics.arc(bx, by, POINT_RADIUS, 0, Math.PI * 2);\n"
        "        mgraphics.fill();\n"
        "    }\n"
        "}\n"
    )
