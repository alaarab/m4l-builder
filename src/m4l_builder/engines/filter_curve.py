"""Filter curve display engine for jsui.

Generates JavaScript code that renders an interactive filter frequency response
curve inside Max's jsui object using mgraphics (Cairo-based vector graphics).

Inlets:
    0 — Frequency (Hz), float 20–20000
    1 — Resonance/Q, float 0.0–1.0
    2 — Filter type, int 0–6 (LP, HP, BP, Notch, Peak, LowShelf, HighShelf)
"""

FILTER_CURVE_INLETS = 3
FILTER_CURVE_OUTLETS = 0


def filter_curve_js(*, bg_color="0.07, 0.07, 0.08, 1.0",
                    line_color="0.45, 0.75, 0.65, 1.0",
                    fill_color="0.45, 0.75, 0.65, 0.15",
                    grid_color="0.2, 0.2, 0.22, 0.5",
                    text_color="0.5, 0.5, 0.52, 1.0",
                    cursor_color="0.8, 0.8, 0.8, 0.4"):
    """Return JavaScript source for a filter curve display (Max jsui).

    The JS code draws a logarithmic frequency response curve for 7 filter
    types using 2nd-order analog prototype transfer functions. It features
    grid lines, axis labels, a frequency cursor, and a semi-transparent
    fill between the curve and the 0 dB line.

    All color arguments are RGBA strings, e.g. ``"0.45, 0.75, 0.65, 1.0"``.

    Args:
        bg_color: Background color.
        line_color: Response curve line color.
        fill_color: Semi-transparent fill under/over the curve.
        grid_color: Grid line color.
        text_color: Axis label color.
        cursor_color: Vertical frequency cursor color.

    Returns:
        JavaScript source code string (ES5 compatible).
    """
    # Parse grid_color to compute brighter (0dB line) and dimmer (minor grid) variants
    gc = [s.strip() for s in grid_color.split(",")]
    grid_bright = ", ".join(gc[:3] + [str(min(1.0, float(gc[3]) * 1.6))])
    grid_dim = ", ".join(gc[:3] + [str(float(gc[3]) * 0.35)])

    return (
        "// Filter Curve Display — generated by m4l-builder\n"
        "// ES5 JavaScript for Max jsui mgraphics\n"
        "\n"
        "mgraphics.init();\n"
        "mgraphics.relative_coords = 0;\n"
        "mgraphics.autofill = 0;\n"
        "\n"
        "inlets = 3;\n"
        "outlets = 0;\n"
        "\n"
        "// --- State ---\n"
        "var freq = 1000;\n"
        "var res = 0.5;\n"
        "var filter_type = 0;\n"
        "\n"
        "// --- Constants ---\n"
        "var MIN_FREQ = 20;\n"
        "var MAX_FREQ = 20000;\n"
        "var MIN_DB = -24;\n"
        "var MAX_DB = 24;\n"
        "var NUM_POINTS = 256;\n"
        "var MARGIN_LEFT = 28;\n"
        "var MARGIN_RIGHT = 8;\n"
        "var MARGIN_TOP = 6;\n"
        "var MARGIN_BOTTOM = 16;\n"
        "var LOG_MIN = Math.log(MIN_FREQ) / Math.LN10;\n"
        "var LOG_MAX = Math.log(MAX_FREQ) / Math.LN10;\n"
        "var LOG_RANGE = LOG_MAX - LOG_MIN;\n"
        "\n"
        "// Filter types\n"
        "var LP = 0, HP = 1, BP = 2, NOTCH = 3, PEAK = 4, LSHELF = 5, HSHELF = 6;\n"
        "\n"
        "// --- Input handlers ---\n"
        "function msg_float(v) {\n"
        "    if (inlet == 0) freq = Math.max(MIN_FREQ, Math.min(MAX_FREQ, v));\n"
        "    else if (inlet == 1) res = Math.max(0.0, Math.min(1.0, v));\n"
        "    else if (inlet == 2) filter_type = Math.floor(v);\n"
        "    mgraphics.redraw();\n"
        "}\n"
        "function msg_int(v) { msg_float(v); }\n"
        "\n"
        "// --- Mapping helpers ---\n"
        "function freq_to_x(f, plot_x, plot_w) {\n"
        "    var logf = Math.log(f) / Math.LN10;\n"
        "    return plot_x + ((logf - LOG_MIN) / LOG_RANGE) * plot_w;\n"
        "}\n"
        "\n"
        "function db_to_y(db, plot_y, plot_h) {\n"
        "    var clamped = Math.max(MIN_DB, Math.min(MAX_DB, db));\n"
        "    return plot_y + (1.0 - (clamped - MIN_DB) / (MAX_DB - MIN_DB)) * plot_h;\n"
        "}\n"
        "\n"
        "// --- Filter magnitude (dB) ---\n"
        "function filter_mag_db(f, fc, q01, ftype) {\n"
        "    // q01 is 0..1, map to actual Q: 0.5 .. 20\n"
        "    var Q = 0.5 + q01 * 19.5;\n"
        "    var s = f / fc;\n"
        "    var s2 = s * s;\n"
        "    var denom2 = (1.0 - s2) * (1.0 - s2) + (s / Q) * (s / Q);\n"
        "    var mag = 0;\n"
        "\n"
        "    if (ftype == LP) {\n"
        "        mag = 1.0 / Math.sqrt(denom2);\n"
        "    } else if (ftype == HP) {\n"
        "        mag = s2 / Math.sqrt(denom2);\n"
        "    } else if (ftype == BP) {\n"
        "        mag = (s / Q) / Math.sqrt(denom2);\n"
        "    } else if (ftype == NOTCH) {\n"
        "        mag = Math.abs(1.0 - s2) / Math.sqrt(denom2);\n"
        "    } else if (ftype == PEAK) {\n"
        "        // Peak boost/cut: gain controlled by Q mapping\n"
        "        var G = 1.0 + q01 * 5.0;\n"
        "        var num2 = (1.0 - s2) * (1.0 - s2) + (s * G / Q) * (s * G / Q);\n"
        "        mag = Math.sqrt(num2 / denom2);\n"
        "    } else if (ftype == LSHELF) {\n"
        "        // Low shelf: boost below fc\n"
        "        var G = Math.pow(10, q01 * 12 / 20);\n"
        "        var sqG = Math.sqrt(G);\n"
        "        var w0 = Math.PI * 2 * f / 48000;\n"
        "        var wc = Math.PI * 2 * fc / 48000;\n"
        "        var cosw = Math.cos(wc);\n"
        "        var sinw = Math.sin(wc);\n"
        "        var alpha = sinw / (2.0 * Math.max(0.5, Q));\n"
        "        // Bilinear coefficients for low shelf\n"
        "        var a0 = (G + 1) + (G - 1) * cosw + 2 * sqG * alpha;\n"
        "        var b0 = G * ((G + 1) - (G - 1) * cosw + 2 * sqG * alpha);\n"
        "        var b1 = 2 * G * ((G - 1) - (G + 1) * cosw);\n"
        "        var b2 = G * ((G + 1) - (G - 1) * cosw - 2 * sqG * alpha);\n"
        "        var a1 = -2 * ((G - 1) + (G + 1) * cosw);\n"
        "        var a2 = (G + 1) + (G - 1) * cosw - 2 * sqG * alpha;\n"
        "        // Evaluate frequency response at f\n"
        "        var w = Math.PI * 2 * f / 48000;\n"
        "        var cw = Math.cos(w);\n"
        "        var sw_v = Math.sin(w);\n"
        "        var numRe = b0 + b1 * cw + b2 * (2 * cw * cw - 1);\n"
        "        var numIm = -b1 * sw_v - b2 * 2 * sw_v * cw;\n"
        "        var denRe = a0 + a1 * cw + a2 * (2 * cw * cw - 1);\n"
        "        var denIm = -a1 * sw_v - a2 * 2 * sw_v * cw;\n"
        "        var numMag = Math.sqrt(numRe * numRe + numIm * numIm);\n"
        "        var denMag = Math.sqrt(denRe * denRe + denIm * denIm);\n"
        "        mag = numMag / Math.max(denMag, 1e-12);\n"
        "    } else if (ftype == HSHELF) {\n"
        "        // High shelf: boost above fc\n"
        "        var G = Math.pow(10, q01 * 12 / 20);\n"
        "        var sqG = Math.sqrt(G);\n"
        "        var wc = Math.PI * 2 * fc / 48000;\n"
        "        var cosw = Math.cos(wc);\n"
        "        var sinw = Math.sin(wc);\n"
        "        var alpha = sinw / (2.0 * Math.max(0.5, Q));\n"
        "        var a0 = (G + 1) - (G - 1) * cosw + 2 * sqG * alpha;\n"
        "        var b0 = G * ((G + 1) + (G - 1) * cosw + 2 * sqG * alpha);\n"
        "        var b1 = -2 * G * ((G - 1) + (G + 1) * cosw);\n"
        "        var b2 = G * ((G + 1) + (G - 1) * cosw - 2 * sqG * alpha);\n"
        "        var a1 = 2 * ((G - 1) - (G + 1) * cosw);\n"
        "        var a2 = (G + 1) - (G - 1) * cosw - 2 * sqG * alpha;\n"
        "        var w = Math.PI * 2 * f / 48000;\n"
        "        var cw = Math.cos(w);\n"
        "        var sw_v = Math.sin(w);\n"
        "        var numRe = b0 + b1 * cw + b2 * (2 * cw * cw - 1);\n"
        "        var numIm = -b1 * sw_v - b2 * 2 * sw_v * cw;\n"
        "        var denRe = a0 + a1 * cw + a2 * (2 * cw * cw - 1);\n"
        "        var denIm = -a1 * sw_v - a2 * 2 * sw_v * cw;\n"
        "        var numMag = Math.sqrt(numRe * numRe + numIm * numIm);\n"
        "        var denMag = Math.sqrt(denRe * denRe + denIm * denIm);\n"
        "        mag = numMag / Math.max(denMag, 1e-12);\n"
        "    }\n"
        "\n"
        "    if (mag <= 0) mag = 1e-12;\n"
        "    return 20.0 * Math.log(mag) / Math.LN10;\n"
        "}\n"
        "\n"
        "// --- Paint ---\n"
        "function paint() {\n"
        "    var w = mgraphics.size[0];\n"
        "    var h = mgraphics.size[1];\n"
        "    var plot_x = MARGIN_LEFT;\n"
        "    var plot_y = MARGIN_TOP;\n"
        "    var plot_w = w - MARGIN_LEFT - MARGIN_RIGHT;\n"
        "    var plot_h = h - MARGIN_TOP - MARGIN_BOTTOM;\n"
        "\n"
        "    // --- Background with rounded corners ---\n"
        "    mgraphics.set_source_rgba(" + bg_color + ");\n"
        "    mgraphics.rectangle_rounded(0, 0, w, h, 4, 4);\n"
        "    mgraphics.fill();\n"
        "\n"
        "    // --- dB grid lines ---\n"
        "    mgraphics.set_line_width(1);\n"
        "    var db_lines = [-18, -12, -6, 0, 6, 12, 18];\n"
        "    for (var i = 0; i < db_lines.length; i++) {\n"
        "        var db = db_lines[i];\n"
        "        var y = db_to_y(db, plot_y, plot_h);\n"
        "        if (db == 0) {\n"
        "            mgraphics.set_source_rgba(" + grid_bright + ");\n"
        "        } else {\n"
        "            mgraphics.set_source_rgba(" + grid_color + ");\n"
        "        }\n"
        "        mgraphics.move_to(plot_x, Math.round(y) + 0.5);\n"
        "        mgraphics.line_to(plot_x + plot_w, Math.round(y) + 0.5);\n"
        "        mgraphics.stroke();\n"
        "    }\n"
        "\n"
        "    // --- dB labels ---\n"
        "    mgraphics.select_font_face(\"Ableton Sans Medium\");\n"
        "    mgraphics.set_font_size(8);\n"
        "    mgraphics.set_source_rgba(" + text_color + ");\n"
        "    var db_labels = [-18, -12, -6, 0, 6, 12, 18];\n"
        "    for (var i = 0; i < db_labels.length; i++) {\n"
        "        var db = db_labels[i];\n"
        "        var y = db_to_y(db, plot_y, plot_h);\n"
        "        var label = (db > 0 ? \"+\" : \"\") + db;\n"
        "        mgraphics.move_to(2, y + 3);\n"
        "        mgraphics.show_text(label);\n"
        "    }\n"
        "\n"
        "    // --- Frequency grid lines ---\n"
        "    var freq_grid = [100, 1000, 10000];\n"
        "    mgraphics.set_source_rgba(" + grid_color + ");\n"
        "    mgraphics.set_line_width(1);\n"
        "    for (var i = 0; i < freq_grid.length; i++) {\n"
        "        var x = freq_to_x(freq_grid[i], plot_x, plot_w);\n"
        "        mgraphics.move_to(Math.round(x) + 0.5, plot_y);\n"
        "        mgraphics.line_to(Math.round(x) + 0.5, plot_y + plot_h);\n"
        "        mgraphics.stroke();\n"
        "    }\n"
        "\n"
        "    // --- Minor frequency grid (subtle) ---\n"
        "    var freq_minor = [50, 200, 500, 2000, 5000, 20000];\n"
        "    mgraphics.set_source_rgba(" + grid_dim + ");\n"
        "    for (var i = 0; i < freq_minor.length; i++) {\n"
        "        var x = freq_to_x(freq_minor[i], plot_x, plot_w);\n"
        "        mgraphics.move_to(Math.round(x) + 0.5, plot_y);\n"
        "        mgraphics.line_to(Math.round(x) + 0.5, plot_y + plot_h);\n"
        "        mgraphics.stroke();\n"
        "    }\n"
        "\n"
        "    // --- Frequency labels ---\n"
        "    mgraphics.select_font_face(\"Ableton Sans Medium\");\n"
        "    mgraphics.set_font_size(8);\n"
        "    mgraphics.set_source_rgba(" + text_color + ");\n"
        "    var freq_labels = [\n"
        "        [100, \"100\"],\n"
        "        [1000, \"1k\"],\n"
        "        [10000, \"10k\"]\n"
        "    ];\n"
        "    for (var i = 0; i < freq_labels.length; i++) {\n"
        "        var x = freq_to_x(freq_labels[i][0], plot_x, plot_w);\n"
        "        mgraphics.move_to(x - 6, h - 3);\n"
        "        mgraphics.show_text(freq_labels[i][1]);\n"
        "    }\n"
        "\n"
        "    // --- Compute response curve ---\n"
        "    var xs = [];\n"
        "    var ys = [];\n"
        "    var zero_y = db_to_y(0, plot_y, plot_h);\n"
        "\n"
        "    for (var i = 0; i < NUM_POINTS; i++) {\n"
        "        var t = i / (NUM_POINTS - 1);\n"
        "        var log_f = LOG_MIN + t * LOG_RANGE;\n"
        "        var f = Math.pow(10, log_f);\n"
        "        var db = filter_mag_db(f, freq, res, filter_type);\n"
        "        var px = freq_to_x(f, plot_x, plot_w);\n"
        "        var py = db_to_y(db, plot_y, plot_h);\n"
        "        xs.push(px);\n"
        "        ys.push(py);\n"
        "    }\n"
        "\n"
        "    // --- Fill between curve and 0 dB ---\n"
        "    mgraphics.set_source_rgba(" + fill_color + ");\n"
        "    mgraphics.move_to(xs[0], zero_y);\n"
        "    for (var i = 0; i < xs.length; i++) {\n"
        "        mgraphics.line_to(xs[i], ys[i]);\n"
        "    }\n"
        "    mgraphics.line_to(xs[xs.length - 1], zero_y);\n"
        "    mgraphics.close_path();\n"
        "    mgraphics.fill();\n"
        "\n"
        "    // --- Draw curve line ---\n"
        "    mgraphics.set_source_rgba(" + line_color + ");\n"
        "    mgraphics.set_line_width(2.0);\n"
        "    mgraphics.move_to(xs[0], ys[0]);\n"
        "    for (var i = 1; i < xs.length; i++) {\n"
        "        mgraphics.line_to(xs[i], ys[i]);\n"
        "    }\n"
        "    mgraphics.stroke();\n"
        "\n"
        "    // --- Frequency cursor ---\n"
        "    var cx = freq_to_x(freq, plot_x, plot_w);\n"
        "    if (cx >= plot_x && cx <= plot_x + plot_w) {\n"
        "        mgraphics.set_source_rgba(" + cursor_color + ");\n"
        "        mgraphics.set_line_width(1);\n"
        "        mgraphics.move_to(Math.round(cx) + 0.5, plot_y);\n"
        "        mgraphics.line_to(Math.round(cx) + 0.5, plot_y + plot_h);\n"
        "        mgraphics.stroke();\n"
        "    }\n"
        "\n"
        "    // --- Frequency readout at cursor ---\n"
        "    mgraphics.select_font_face(\"Ableton Sans Medium\");\n"
        "    mgraphics.set_font_size(9);\n"
        "    mgraphics.set_source_rgba(" + text_color + ");\n"
        "    var freq_str;\n"
        "    if (freq >= 1000) {\n"
        "        freq_str = (Math.round(freq / 100) / 10) + \"kHz\";\n"
        "    } else {\n"
        "        freq_str = Math.round(freq) + \"Hz\";\n"
        "    }\n"
        "    var text_x = Math.min(cx + 4, w - 40);\n"
        "    mgraphics.move_to(text_x, plot_y + 12);\n"
        "    mgraphics.show_text(freq_str);\n"
        "}\n"
    )
