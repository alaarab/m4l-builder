"""ADSR envelope display engine for jsui.

Generates JavaScript code that renders an ADSR envelope curve inside Max's
jsui object using mgraphics (Cairo-based vector graphics). The curve uses
exponential shapes for natural-sounding visualization.

Inlets:
    0: Attack time (ms), float 0.1-5000
    1: Decay time (ms), float 0.1-5000
    2: Sustain level, float 0.0-1.0
    3: Release time (ms), float 0.1-10000
    4: Playback position (0.0-1.0), optional dot tracker
"""

ENVELOPE_INLETS = 4
ENVELOPE_OUTLETS = 0


def envelope_display_js(*, bg_color="0.07, 0.07, 0.08, 1.0",
                        line_color="0.85, 0.55, 0.25, 1.0",
                        fill_color="0.85, 0.55, 0.25, 0.15",
                        grid_color="0.2, 0.2, 0.22, 0.5",
                        text_color="0.5, 0.5, 0.52, 1.0",
                        marker_color="0.4, 0.4, 0.42, 0.5"):
    """Return JavaScript source for an ADSR envelope display (Max jsui).

    The JS code draws exponential attack, decay, and release curves with a
    flat sustain segment. It features a semi-transparent fill, segment
    boundary markers, A/D/S/R labels, a horizontal level grid, and an
    optional playback position dot.

    All color arguments are RGBA strings, e.g. ``"0.85, 0.55, 0.25, 1.0"``.

    Args:
        bg_color: Background color.
        line_color: Envelope curve line color.
        fill_color: Semi-transparent fill under the curve.
        grid_color: Horizontal level grid line color.
        text_color: Axis label and segment label color.
        marker_color: Vertical segment boundary marker color.

    Returns:
        JavaScript source code string (ES5 compatible).
    """
    return (
        "// ADSR Envelope Display - generated by m4l-builder\n"
        "// ES5 JavaScript for Max jsui mgraphics\n"
        "\n"
        "mgraphics.init();\n"
        "mgraphics.relative_coords = 0;\n"
        "mgraphics.autofill = 0;\n"
        "\n"
        "inlets = 5;\n"
        "outlets = 0;\n"
        "\n"
        "// --- State ---\n"
        "var attack_ms = 10;\n"
        "var decay_ms = 100;\n"
        "var sustain = 0.7;\n"
        "var release_ms = 200;\n"
        "var play_pos = -1;\n"
        "\n"
        "// --- Constants ---\n"
        "var MARGIN_L = 20;\n"
        "var MARGIN_R = 8;\n"
        "var MARGIN_T = 8;\n"
        "var MARGIN_B = 18;\n"
        "var SUSTAIN_FRAC = 0.2;\n"
        "var CURVE_STEPS = 50;\n"
        "\n"
        "// --- Input handlers ---\n"
        "function msg_float(v) {\n"
        "    if (inlet == 0) attack_ms = Math.max(0.1, v);\n"
        "    else if (inlet == 1) decay_ms = Math.max(0.1, v);\n"
        "    else if (inlet == 2) sustain = Math.max(0, Math.min(1, v));\n"
        "    else if (inlet == 3) release_ms = Math.max(0.1, v);\n"
        "    else if (inlet == 4) play_pos = Math.max(0, Math.min(1, v));\n"
        "    mgraphics.redraw();\n"
        "}\n"
        "function msg_int(v) { msg_float(v); }\n"
        "\n"
        "// --- Level-to-Y mapping ---\n"
        "function level_to_y(level, plot_y, plot_h) {\n"
        "    return plot_y + (1.0 - level) * plot_h;\n"
        "}\n"
        "\n"
        "// --- Build envelope point arrays ---\n"
        "// Returns array of {x, y} pixel coords across all segments\n"
        "function build_envelope(plot_x, plot_y, plot_w, plot_h) {\n"
        "    var total_ms = attack_ms + decay_ms + release_ms;\n"
        "    var sustain_ms = (total_ms / (1.0 - SUSTAIN_FRAC)) * SUSTAIN_FRAC;\n"
        "    var full_ms = total_ms + sustain_ms;\n"
        "\n"
        "    // Pixel boundaries for each segment\n"
        "    var x_start  = plot_x;\n"
        "    var x_a_end  = plot_x + (attack_ms  / full_ms) * plot_w;\n"
        "    var x_d_end  = plot_x + ((attack_ms + decay_ms) / full_ms) * plot_w;\n"
        "    var x_s_end  = plot_x + ((attack_ms + decay_ms + sustain_ms) / full_ms) * plot_w;\n"
        "    var x_end    = plot_x + plot_w;\n"
        "\n"
        "    var pts = [];\n"
        "\n"
        "    // Attack: exponential rise 0 -> 1\n"
        "    for (var i = 0; i <= CURVE_STEPS; i++) {\n"
        "        var t = i / CURVE_STEPS;\n"
        "        var level = 1.0 - Math.exp(-t * 5.0);\n"
        "        var px = x_start + t * (x_a_end - x_start);\n"
        "        var py = level_to_y(level, plot_y, plot_h);\n"
        "        pts.push([px, py]);\n"
        "    }\n"
        "\n"
        "    // Decay: exponential fall 1 -> sustain\n"
        "    for (var i = 1; i <= CURVE_STEPS; i++) {\n"
        "        var t = i / CURVE_STEPS;\n"
        "        var level = sustain + (1.0 - sustain) * Math.exp(-t * 5.0);\n"
        "        var px = x_a_end + t * (x_d_end - x_a_end);\n"
        "        var py = level_to_y(level, plot_y, plot_h);\n"
        "        pts.push([px, py]);\n"
        "    }\n"
        "\n"
        "    // Sustain: flat line at sustain level\n"
        "    var sus_y = level_to_y(sustain, plot_y, plot_h);\n"
        "    pts.push([x_d_end, sus_y]);\n"
        "    pts.push([x_s_end, sus_y]);\n"
        "\n"
        "    // Release: exponential fall sustain -> 0\n"
        "    for (var i = 1; i <= CURVE_STEPS; i++) {\n"
        "        var t = i / CURVE_STEPS;\n"
        "        var level = sustain * Math.exp(-t * 5.0);\n"
        "        var px = x_s_end + t * (x_end - x_s_end);\n"
        "        var py = level_to_y(level, plot_y, plot_h);\n"
        "        pts.push([px, py]);\n"
        "    }\n"
        "\n"
        "    return {\n"
        "        pts: pts,\n"
        "        x_a_end: x_a_end,\n"
        "        x_d_end: x_d_end,\n"
        "        x_s_end: x_s_end\n"
        "    };\n"
        "}\n"
        "\n"
        "// --- Dashed line helper (dotted vertical marker) ---\n"
        "function draw_dashed_vertical(x, y_top, y_bot, dash, gap) {\n"
        "    var y = y_top;\n"
        "    var on = true;\n"
        "    while (y < y_bot) {\n"
        "        var seg_len = on ? dash : gap;\n"
        "        var y_next = Math.min(y + seg_len, y_bot);\n"
        "        if (on) {\n"
        "            mgraphics.move_to(x + 0.5, y);\n"
        "            mgraphics.line_to(x + 0.5, y_next);\n"
        "            mgraphics.stroke();\n"
        "        }\n"
        "        y = y_next;\n"
        "        on = !on;\n"
        "    }\n"
        "}\n"
        "\n"
        "// --- Paint ---\n"
        "function paint() {\n"
        "    var w = mgraphics.size[0];\n"
        "    var h = mgraphics.size[1];\n"
        "    var plot_x = MARGIN_L;\n"
        "    var plot_y = MARGIN_T;\n"
        "    var plot_w = w - MARGIN_L - MARGIN_R;\n"
        "    var plot_h = h - MARGIN_T - MARGIN_B;\n"
        "\n"
        "    // --- Background ---\n"
        "    mgraphics.set_source_rgba(" + bg_color + ");\n"
        "    mgraphics.rectangle_rounded(0, 0, w, h, 4, 4);\n"
        "    mgraphics.fill();\n"
        "\n"
        "    // --- Horizontal level grid (0.25, 0.5, 0.75, 1.0) ---\n"
        "    mgraphics.set_line_width(1);\n"
        "    var grid_levels = [0.25, 0.5, 0.75, 1.0];\n"
        "    for (var i = 0; i < grid_levels.length; i++) {\n"
        "        var gy = level_to_y(grid_levels[i], plot_y, plot_h);\n"
        "        mgraphics.set_source_rgba(" + grid_color + ");\n"
        "        mgraphics.move_to(plot_x, Math.round(gy) + 0.5);\n"
        "        mgraphics.line_to(plot_x + plot_w, Math.round(gy) + 0.5);\n"
        "        mgraphics.stroke();\n"
        "    }\n"
        "\n"
        "    // --- Level labels on left axis ---\n"
        "    mgraphics.select_font_face(\"Ableton Sans Medium\");\n"
        "    mgraphics.set_font_size(7);\n"
        "    mgraphics.set_source_rgba(" + text_color + ");\n"
        "    var level_labels = [[1.0, \"1\"], [0.75, \".75\"], [0.5, \".5\"], [0.25, \".25\"]];\n"
        "    for (var i = 0; i < level_labels.length; i++) {\n"
        "        var lv = level_labels[i][0];\n"
        "        var ly = level_to_y(lv, plot_y, plot_h);\n"
        "        mgraphics.move_to(2, ly + 3);\n"
        "        mgraphics.show_text(level_labels[i][1]);\n"
        "    }\n"
        "\n"
        "    // --- Build envelope geometry ---\n"
        "    var env = build_envelope(plot_x, plot_y, plot_w, plot_h);\n"
        "    var pts = env.pts;\n"
        "    var x_a_end = env.x_a_end;\n"
        "    var x_d_end = env.x_d_end;\n"
        "    var x_s_end = env.x_s_end;\n"
        "\n"
        "    // --- Fill under envelope ---\n"
        "    var base_y = level_to_y(0, plot_y, plot_h);\n"
        "    mgraphics.set_source_rgba(" + fill_color + ");\n"
        "    mgraphics.move_to(pts[0][0], base_y);\n"
        "    for (var i = 0; i < pts.length; i++) {\n"
        "        mgraphics.line_to(pts[i][0], pts[i][1]);\n"
        "    }\n"
        "    mgraphics.line_to(pts[pts.length - 1][0], base_y);\n"
        "    mgraphics.close_path();\n"
        "    mgraphics.fill();\n"
        "\n"
        "    // --- Draw envelope curve line ---\n"
        "    mgraphics.set_source_rgba(" + line_color + ");\n"
        "    mgraphics.set_line_width(2.0);\n"
        "    mgraphics.move_to(pts[0][0], pts[0][1]);\n"
        "    for (var i = 1; i < pts.length; i++) {\n"
        "        mgraphics.line_to(pts[i][0], pts[i][1]);\n"
        "    }\n"
        "    mgraphics.stroke();\n"
        "\n"
        "    // --- Segment boundary markers (dotted vertical lines) ---\n"
        "    mgraphics.set_source_rgba(" + marker_color + ");\n"
        "    mgraphics.set_line_width(1);\n"
        "    draw_dashed_vertical(x_a_end, plot_y, plot_y + plot_h, 3, 3);\n"
        "    draw_dashed_vertical(x_d_end, plot_y, plot_y + plot_h, 3, 3);\n"
        "    draw_dashed_vertical(x_s_end, plot_y, plot_y + plot_h, 3, 3);\n"
        "\n"
        "    // --- Segment labels A / D / S / R ---\n"
        "    mgraphics.select_font_face(\"Ableton Sans Medium\");\n"
        "    mgraphics.set_font_size(8);\n"
        "    mgraphics.set_source_rgba(" + text_color + ");\n"
        "    var label_y = plot_y + plot_h + 12;\n"
        "\n"
        "    // Center each label in its segment\n"
        "    var cx_a = (plot_x + x_a_end) / 2;\n"
        "    var cx_d = (x_a_end + x_d_end) / 2;\n"
        "    var cx_s = (x_d_end + x_s_end) / 2;\n"
        "    var cx_r = (x_s_end + plot_x + plot_w) / 2;\n"
        "\n"
        "    mgraphics.move_to(cx_a - 3, label_y);\n"
        "    mgraphics.show_text(\"A\");\n"
        "    mgraphics.move_to(cx_d - 3, label_y);\n"
        "    mgraphics.show_text(\"D\");\n"
        "    mgraphics.move_to(cx_s - 3, label_y);\n"
        "    mgraphics.show_text(\"S\");\n"
        "    mgraphics.move_to(cx_r - 3, label_y);\n"
        "    mgraphics.show_text(\"R\");\n"
        "\n"
        "    // --- Playback position dot (inlet 4) ---\n"
        "    if (play_pos >= 0) {\n"
        "        var dot_x = plot_x + play_pos * plot_w;\n"
        "        // Find the envelope Y at this X by linear interp through pts\n"
        "        var dot_y = pts[pts.length - 1][1];\n"
        "        for (var i = 1; i < pts.length; i++) {\n"
        "            if (pts[i][0] >= dot_x) {\n"
        "                var t_seg = (dot_x - pts[i-1][0]) / (pts[i][0] - pts[i-1][0] + 0.0001);\n"
        "                dot_y = pts[i-1][1] + t_seg * (pts[i][1] - pts[i-1][1]);\n"
        "                break;\n"
        "            }\n"
        "        }\n"
        "        mgraphics.set_source_rgba(" + line_color + ");\n"
        "        mgraphics.arc(dot_x, dot_y, 3, 0, 6.2832);\n"
        "        mgraphics.fill();\n"
        "        // Bright highlight ring\n"
        "        mgraphics.set_source_rgba(1.0, 1.0, 1.0, 0.6);\n"
        "        mgraphics.set_line_width(1.0);\n"
        "        mgraphics.arc(dot_x, dot_y, 3, 0, 6.2832);\n"
        "        mgraphics.stroke();\n"
        "    }\n"
        "}\n"
    )
