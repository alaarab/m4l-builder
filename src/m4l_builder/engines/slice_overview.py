"""Slice overview display engine for jsui.

Renders a sampler-style overview with a highlighted region, slice dividers,
and a moving playhead marker. When possible, the display samples a named Max
buffer directly so the waveform reflects the loaded source file; if the buffer
is unavailable, it falls back to a synthetic preview shape.

Inlets:
    0 -- source loaded flag (0/1)
    1 -- region start (float 0.0-1.0)
    2 -- region end (float 0.0-1.0)
    3 -- playhead position (float 0.0-1.0)
    4 -- slice count (float/int, clamped 1-64)

Outlets:
    0 -- clicked position (float 0.0-1.0)
"""

SLICE_OVERVIEW_INLETS = 5
SLICE_OVERVIEW_OUTLETS = 1


def slice_overview_js(
    *,
    buffer_name="slicebuf",
    bg_color="0.07, 0.07, 0.08, 1.0",
    grid_color="0.18, 0.18, 0.20, 1.0",
    wave_color="0.52, 0.64, 0.78, 0.52",
    region_fill="0.90, 0.67, 0.28, 0.14",
    region_line="0.90, 0.67, 0.28, 0.88",
    playhead_color="0.98, 0.95, 0.82, 0.95",
    text_color="0.72, 0.74, 0.78, 0.90",
):
    """Return JavaScript source for a sampler-style slice overview."""
    return (
        "// Slice Overview - generated by m4l-builder\n"
        "// ES5 JavaScript for Max jsui mgraphics\n"
        "\n"
        "mgraphics.init();\n"
        "mgraphics.relative_coords = 0;\n"
        "mgraphics.autofill = 0;\n"
        "\n"
        "inlets = 5;\n"
        "outlets = 1;\n"
        "\n"
        "var loaded = 0;\n"
        "var region_start = 0.0;\n"
        "var region_end = 1.0;\n"
        "var playhead = 0.0;\n"
        "var slice_count = 16;\n"
        "var PADDING = 8;\n"
        "var GRID_DIVS = 16;\n"
        "var BUFFER_NAME = '" + buffer_name + "';\n"
        "var WAVE_POINTS = 256;\n"
        "var waveform_cache = [];\n"
        "var waveform_ready = 0;\n"
        "\n"
        "function clamp(v, lo, hi) {\n"
        "    if (v < lo) return lo;\n"
        "    if (v > hi) return hi;\n"
        "    return v;\n"
        "}\n"
        "\n"
        "function norm_to_x(v, x, w) {\n"
        "    return x + clamp(v, 0.0, 1.0) * w;\n"
        "}\n"
        "\n"
        "function msg_int(v) {\n"
        "    msg_float(v);\n"
        "}\n"
        "\n"
        "function msg_float(v) {\n"
        "    if (inlet === 0) {\n"
        "        loaded = v > 0 ? 1 : 0;\n"
        "        rebuild_waveform_cache();\n"
        "    } else if (inlet === 1) {\n"
        "        region_start = clamp(v, 0.0, 1.0);\n"
        "        if (region_end < region_start) region_end = region_start;\n"
        "    } else if (inlet === 2) {\n"
        "        region_end = clamp(v, 0.0, 1.0);\n"
        "        if (region_end < region_start) region_start = region_end;\n"
        "    } else if (inlet === 3) {\n"
        "        playhead = clamp(v, 0.0, 1.0);\n"
        "    } else if (inlet === 4) {\n"
        "        slice_count = Math.max(1, Math.min(64, Math.round(v)));\n"
        "    }\n"
        "    mgraphics.redraw();\n"
        "}\n"
        "\n"
        "function rebuild_waveform_cache() {\n"
        "    waveform_cache = [];\n"
        "    waveform_ready = 0;\n"
        "    if (!loaded) return;\n"
        "    try {\n"
        "        var source_buffer = new Buffer(BUFFER_NAME);\n"
        "        var frame_count = source_buffer.framecount();\n"
        "        var channel_count = source_buffer.channelcount();\n"
        "        if (!frame_count || frame_count <= 0 || !channel_count || channel_count <= 0) return;\n"
        "        for (var i = 0; i < WAVE_POINTS; i++) {\n"
        "            var start_frame = Math.floor((i / WAVE_POINTS) * frame_count);\n"
        "            var end_frame = Math.floor(((i + 1) / WAVE_POINTS) * frame_count);\n"
        "            if (end_frame <= start_frame) end_frame = start_frame + 1;\n"
        "            if (end_frame > frame_count) end_frame = frame_count;\n"
        "            var stride = Math.max(1, Math.floor((end_frame - start_frame) / 8));\n"
        "            var peak = 0.0;\n"
        "            for (var frame = start_frame; frame < end_frame; frame += stride) {\n"
        "                var sample_sum = 0.0;\n"
        "                for (var channel = 1; channel <= channel_count; channel++) {\n"
        "                    var sample_value = source_buffer.peek(channel, frame);\n"
        "                    if (sample_value < 0.0) sample_value = -sample_value;\n"
        "                    sample_sum += sample_value;\n"
        "                }\n"
        "                sample_sum = sample_sum / channel_count;\n"
        "                if (sample_sum > peak) peak = sample_sum;\n"
        "            }\n"
        "            waveform_cache[i] = clamp(peak, 0.0, 1.0);\n"
        "        }\n"
        "        waveform_ready = waveform_cache.length > 0 ? 1 : 0;\n"
        "    } catch (err) {\n"
        "        waveform_ready = 0;\n"
        "    }\n"
        "}\n"
        "\n"
        "function onclick(x, y, but, cmd, shift, capslock, option, ctrl) {\n"
        "    var w = mgraphics.size[0];\n"
        "    var inner_x = PADDING;\n"
        "    var inner_w = w - PADDING * 2;\n"
        "    var pos = clamp((x - inner_x) / inner_w, 0.0, 1.0);\n"
        "    outlet(0, pos);\n"
        "}\n"
        "\n"
        "function ondrag(x, y, but, cmd, shift, capslock, option, ctrl) {\n"
        "    if (!but) return;\n"
        "    onclick(x, y, but, cmd, shift, capslock, option, ctrl);\n"
        "}\n"
        "\n"
        "function draw_waveform(x, y, w, h) {\n"
        "    var mid = y + h * 0.5;\n"
        "    var amp = h * 0.38;\n"
        "    mgraphics.set_source_rgba(" + wave_color + ");\n"
        "    mgraphics.set_line_width(1.2);\n"
        "    if (waveform_ready) {\n"
        "        for (var i = 0; i < waveform_cache.length; i++) {\n"
        "            var t = waveform_cache.length <= 1 ? 0.0 : i / (waveform_cache.length - 1);\n"
        "            var nx = x + t * w;\n"
        "            var ny = mid - waveform_cache[i] * amp;\n"
        "            if (i === 0) {\n"
        "                mgraphics.move_to(nx, ny);\n"
        "            } else {\n"
        "                mgraphics.line_to(nx, ny);\n"
        "            }\n"
        "        }\n"
        "        mgraphics.stroke();\n"
        "        mgraphics.set_source_rgba(" + wave_color + ");\n"
        "        for (var j = 0; j < waveform_cache.length; j++) {\n"
        "            var tt = waveform_cache.length <= 1 ? 0.0 : j / (waveform_cache.length - 1);\n"
        "            var px = x + tt * w;\n"
        "            var py = mid + waveform_cache[j] * amp;\n"
        "            if (j === 0) {\n"
        "                mgraphics.move_to(px, py);\n"
        "            } else {\n"
        "                mgraphics.line_to(px, py);\n"
        "            }\n"
        "        }\n"
        "        mgraphics.stroke();\n"
        "    } else {\n"
        "        for (var k = 0; k < 3; k++) {\n"
        "            var phase = k * 0.63;\n"
        "            var scale = 0.35 + k * 0.17;\n"
        "            var points = 160;\n"
        "            for (var p = 0; p < points; p++) {\n"
        "                var t0 = p / (points - 1);\n"
        "                var v = Math.sin((t0 * 7.0 + phase) * 6.28318) * scale;\n"
        "                v += Math.sin((t0 * 15.0 + phase * 0.7) * 6.28318) * 0.14;\n"
        "                v += Math.cos((t0 * 3.2 + phase * 0.31) * 6.28318) * 0.18;\n"
        "                var sx = x + t0 * w;\n"
        "                var sy = mid - v * amp;\n"
        "                if (p === 0) {\n"
        "                    mgraphics.move_to(sx, sy);\n"
        "                } else {\n"
        "                    mgraphics.line_to(sx, sy);\n"
        "                }\n"
        "            }\n"
        "            mgraphics.stroke();\n"
        "            mgraphics.set_source_rgba(" + wave_color + ");\n"
        "        }\n"
        "    }\n"
        "}\n"
        "\n"
        "function paint() {\n"
        "    var w = mgraphics.size[0];\n"
        "    var h = mgraphics.size[1];\n"
        "    var x = PADDING;\n"
        "    var y = PADDING;\n"
        "    var inner_w = w - PADDING * 2;\n"
        "    var inner_h = h - PADDING * 2;\n"
        "    var i;\n"
        "\n"
        "    mgraphics.set_source_rgba(" + bg_color + ");\n"
        "    mgraphics.rectangle_rounded(0, 0, w, h, 8, 8);\n"
        "    mgraphics.fill();\n"
        "\n"
        "    mgraphics.set_source_rgba(" + grid_color + ");\n"
        "    mgraphics.set_line_width(1.0);\n"
        "    for (i = 0; i <= GRID_DIVS; i++) {\n"
        "        var gx = x + (i / GRID_DIVS) * inner_w;\n"
        "        mgraphics.move_to(gx, y + 2);\n"
        "        mgraphics.line_to(gx, y + inner_h - 2);\n"
        "        mgraphics.stroke();\n"
        "    }\n"
        "    for (i = 0; i < 3; i++) {\n"
        "        var gy = y + ((i + 1) / 4.0) * inner_h;\n"
        "        mgraphics.move_to(x, gy);\n"
        "        mgraphics.line_to(x + inner_w, gy);\n"
        "        mgraphics.stroke();\n"
        "    }\n"
        "\n"
        "    if (!loaded) {\n"
        "        mgraphics.set_source_rgba(" + text_color + ");\n"
        "        mgraphics.select_font_face('Ableton Sans Medium');\n"
        "        mgraphics.set_font_size(12);\n"
        "        mgraphics.move_to(x + 16, y + 24);\n"
        "        mgraphics.show_text('DROP SAMPLE');\n"
        "        mgraphics.set_font_size(9);\n"
        "        mgraphics.move_to(x + 16, y + 42);\n"
        "        mgraphics.show_text('Load a long texture, loop, vocal, or phrase');\n"
        "        mgraphics.move_to(x + 16, y + 58);\n"
        "        mgraphics.show_text('Click or drag to set the scan position');\n"
        "    } else {\n"
        "        draw_waveform(x, y, inner_w, inner_h);\n"
        "\n"
        "        var rs = norm_to_x(region_start, x, inner_w);\n"
        "        var re = norm_to_x(region_end, x, inner_w);\n"
        "        if (re < rs) {\n"
        "            var tmp = rs;\n"
        "            rs = re;\n"
        "            re = tmp;\n"
        "        }\n"
        "\n"
        "        mgraphics.set_source_rgba(" + region_fill + ");\n"
        "        mgraphics.rectangle(rs, y + 2, Math.max(1, re - rs), inner_h - 4);\n"
        "        mgraphics.fill();\n"
        "\n"
        "        mgraphics.set_source_rgba(" + region_line + ");\n"
        "        mgraphics.set_line_width(1.4);\n"
        "        mgraphics.move_to(rs, y + 2);\n"
        "        mgraphics.line_to(rs, y + inner_h - 2);\n"
        "        mgraphics.stroke();\n"
        "        mgraphics.move_to(re, y + 2);\n"
        "        mgraphics.line_to(re, y + inner_h - 2);\n"
        "        mgraphics.stroke();\n"
        "\n"
        "        var width = Math.max(1, re - rs);\n"
        "        var divs = Math.max(1, slice_count);\n"
        "        var max_visible_divs = divs > 32 ? 32 : divs;\n"
        "        var skip = Math.max(1, Math.floor(divs / max_visible_divs));\n"
        "        for (i = 1; i < divs; i += skip) {\n"
        "            var dx = rs + (i / divs) * width;\n"
        "            mgraphics.set_source_rgba(" + region_line + ");\n"
        "            mgraphics.set_line_width(0.8);\n"
        "            mgraphics.move_to(dx, y + 10);\n"
        "            mgraphics.line_to(dx, y + inner_h - 10);\n"
        "            mgraphics.stroke();\n"
        "        }\n"
        "\n"
        "        var ph = norm_to_x(playhead, x, inner_w);\n"
        "        mgraphics.set_source_rgba(" + playhead_color + ");\n"
        "        mgraphics.set_line_width(2.0);\n"
        "        mgraphics.move_to(ph, y);\n"
        "        mgraphics.line_to(ph, y + inner_h);\n"
        "        mgraphics.stroke();\n"
        "        mgraphics.move_to(ph - 4, y + 2);\n"
        "        mgraphics.line_to(ph + 4, y + 2);\n"
        "        mgraphics.line_to(ph, y + 10);\n"
        "        mgraphics.close_path();\n"
        "        mgraphics.fill();\n"
        "\n"
        "        mgraphics.set_source_rgba(" + text_color + ");\n"
        "        mgraphics.select_font_face('Ableton Sans Medium');\n"
        "        mgraphics.set_font_size(9);\n"
        "        mgraphics.move_to(x + 10, y + 16);\n"
        "        mgraphics.show_text('REGION');\n"
        "        mgraphics.move_to(x + inner_w - 86, y + 16);\n"
        "        mgraphics.show_text('SLICES ' + slice_count);\n"
        "    }\n"
        "\n"
        "    mgraphics.set_source_rgba(" + grid_color + ");\n"
        "    mgraphics.set_line_width(1.0);\n"
        "    mgraphics.rectangle_rounded(0.5, 0.5, w - 1, h - 1, 8, 8);\n"
        "    mgraphics.stroke();\n"
        "}\n"
    )
